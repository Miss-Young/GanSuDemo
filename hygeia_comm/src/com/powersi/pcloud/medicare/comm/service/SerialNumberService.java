package com.powersi.pcloud.medicare.comm.service;

/**
 * 序列号
 * 
 * @author 刘勇
 *
 */
public interface SerialNumberService {

	/**
	 * 取递增的序列号
	 * 
	 * @param column
	 * @param akb020
	 * @return
	 */
	public String getSerialNo(String column, String akb020);

	/**
	 * 取递增的序列号（按年循环递增）
	 * 
	 * @param column
	 * @param akb020
	 * @return
	 */
	public String getSerialNoByYear(String column, String akb020);

	/**
	 * 取递增的序列号（按月循环递增）
	 * 
	 * @param column
	 * @param akb020
	 * @return
	 */
	public String getSerialNoByMonth(String column, String akb020);

	/**
	 * 取递增的序列号（每天循环递增）
	 * 
	 * @param column
	 * @param akb020
	 * @return
	 */
	public String getSerialNoByDay(String column, String akb020);

}
